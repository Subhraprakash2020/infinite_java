USE COLLEGE_SYSTEM;

CREATE TABLE Batches (
    BCODE VARCHAR(5) PRIMARY KEY,
    CCODE VARCHAR(5),
    faacode VARCHAR(5),
    STDATE DATE,
    ENDDATE DATE,
    TIMING INT(1),
    FOREIGN KEY (CCODE) REFERENCES Courses(CCODE),
    FOREIGN KEY (faacode) REFERENCES faculity(faacode),
    CHECK (STDATE <= ENDDATE),
    CHECK (TIMING IN (1, 2, 3))
);

DESCRIBE BATCHES;

